func RepoAllLinks() []Link {
	var links []Link
	c, session := connection.GetCollection("links")
	defer session.Close()
	err := c.Find(nil).All(&links)
	if err == nil {
		return links
	} else {
		return links
	}
}

func RepoFindLink(hash string) Link {

	c, session := connection.GetCollection("links")
	defer session.Close()
	result := Link{}
	err := c.Find(bson.M{"hash": hash}).One(&result)
	if err != nil {
		return result
		log.Fatal(err)
	}
	
	return result
}

